    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        // TODO add your handling code here:
        String kd_barang = txtKode.getText();
        String nama_barang = txtNama.getText();
        String tgl_masuk = txtTanggalMasuk.getText();
        String jml = txtJumlah.getText();
        String kondisi = cKondisi.getSelectedItem().toString();
        
        try{ 
            Connection c = Koneksi.getKoneksi(); 
            String sql = "UPDATE inventory_barang_masuk SET Nama_Barang = ?, Tanggal_Masuk = ?, Jumlah = ?, Kondisi = ? where Kd_Barang = ?";  
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama_barang);
            p.setDate(2, java.sql.Date.valueOf(tgl_masuk));
            p.setInt(3, Integer.parseInt(jml));
            p.setString(4, kondisi);
            p.setInt(5, Integer.parseInt(kd_barang));
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(this, "Data berhasil dirubah!");
            bBarangMasuk.setEnabled(true);
            bUpdate.setEnabled(false);
            bHapusMasuk.setEnabled(false);
        } catch(SQLException e) { 
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan"); 
        }
        refreshTabelMasuk();
        loadBarangMasuk();
        refreshText();
    }//GEN-LAST:event_bUpdateActionPerformed

