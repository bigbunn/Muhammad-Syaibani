    private void bBarangKeluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBarangKeluarActionPerformed
        // TODO add your handling code here:
        String kd_barang = txtKode.getText();
        String nama_barang = txtNama.getText();
        String tgl_masuk = txtTanggalMasuk.getText();
        String tgl_keluar = txtTanggalKeluar.getText();
        String jml = txtJumlah.getText();
        String kondisi = cKondisi.getSelectedItem().toString();
        
        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "INSERT INTO inventory_barang_keluar(Kd_Barang,Nama_Barang,Tanggal_Masuk,Tanggal_Keluar,Jumlah,Kondisi) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setInt(1, Integer.parseInt(kd_barang));
            p.setString(2, nama_barang);
            p.setDate(3, Date.valueOf(tgl_masuk));
            p.setDate(4, Date.valueOf(tgl_keluar));
            p.setInt(5, Integer.parseInt(jml));
            p.setString(6, kondisi);
            p.executeUpdate();
            p.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Kesalahan"); 
        }
        refreshTabelKeluar();
        loadBarangKeluar();
        refreshText();
    }//GEN-LAST:event_bBarangKeluarActionPerformed

