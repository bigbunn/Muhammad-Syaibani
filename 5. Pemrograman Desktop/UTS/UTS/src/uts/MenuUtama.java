    private void loadBarangMasuk()  {
        try { 
            Connection c = Koneksi.getKoneksi(); 
            Statement s = c.createStatement(); 
            String sql = "SELECT * FROM inventory_barang_masuk"; 
            ResultSet r = s.executeQuery(sql);
            while (r.next()) { 
            // lakukan penelusuran baris 
                String Kd_Barang = r.getString("Kd_Barang");
                String Nama_Barang = r.getString("Nama_Barang");
                String Tanggal_Masuk = r.getString("Tanggal_Masuk");
                String Jumlah = r.getString("Jumlah");
                String Kondisi = r.getString("Kondisi");
                String tbData[] = {Kd_Barang,Nama_Barang,Tanggal_Masuk,Jumlah,Kondisi};
                DefaultTableModel tblModel = (DefaultTableModel)tblBarangMasuk.getModel();
                tblModel.addRow(tbData);
            } 
            r.close(); 
            s.close(); 
        } catch(SQLException e) { 
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan!"); 
        } 
    }
    
