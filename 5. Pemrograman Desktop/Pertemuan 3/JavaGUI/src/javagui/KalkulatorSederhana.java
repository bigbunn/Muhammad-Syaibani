    private void btnHasilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHasilActionPerformed
        // TODO add your handling code here:
        if (txtNilai.getText().equals(""))  {
            JOptionPane.showMessageDialog(null, "belum diinputkan nilai");
            txtNilai.requestFocus();
        }   else    {
            nilai2 = Double.parseDouble(txtNilai.getText());
            if (flag==1)    {
                hasil = nilai + nilai2;
            } else if (flag==2) {
                hasil = nilai - nilai2;
            }   else if (flag==3)   {
                hasil = nilai * nilai2;
            }   else if (flag==4)   {
                hasil = nilai / nilai2;
            }
            txtNilai.setText(Double.toString(hasil));
        }
    }//GEN-LAST:event_btnHasilActionPerformed

