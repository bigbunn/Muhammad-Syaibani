    private void loadData(){
        try{ 
            Connection c = Koneksi.getKoneksi(); 
            Statement s = c.createStatement(); 
        
            String sql = "SELECT * FROM MAHASISWA"; 
            ResultSet r = s.executeQuery(sql); 
        while (r.next()) { 
            // lakukan penelusuran baris 
            String id = r.getString("id");
            String nama = r.getString("nama");
            String jurusan = r.getString("jurusan");
            String email = r.getString("email");
            String alamat = r.getString("alamat");
            String tbData[] = {id,nama,jurusan,email,alamat};
            DefaultTableModel tblModel = (DefaultTableModel)tabelMahasiswa.getModel();
            tblModel.addRow(tbData);
            } 
        r.close(); 
        s.close(); 
        } catch(SQLException e) { 
            System.out.println("Terjadi Kesalahan"); 
        } 
    }

    private void refreshTabel(){
        DefaultTableModel model =(DefaultTableModel) 
        tabelMahasiswa.getModel();
        while(model.getRowCount()>0){
            model.setRowCount(0);
        } 
    }
    
