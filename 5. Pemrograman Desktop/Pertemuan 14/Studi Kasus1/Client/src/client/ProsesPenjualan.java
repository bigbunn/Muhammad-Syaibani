    private void select (javax.swing.JTable tblBarang1) throws RemoteException {
        // begin
        String header[]={" Kode "," Nama "," Jumlah "};
        java.util.List list = servicesBarang.getBarang();
        Object data[][] = new Object[list.size()][3];
        int x = 0;
        for (Iterator it = list.iterator(); it.hasNext();) {
        StokBarang b = (StokBarang) it.next();
        data[x][0] = b.getKode();
        data[x][1] = b.getNama();
        data[x][2] = b.getJumlah();
        ++x;
    }
    tblBarang1.setModel(new DefaultTableModel(data, header));
    JTableHeader head = tblBarang1.getTableHeader();
    head.setBackground(Color.orange);
    // end
    }

    private void update(ServicesStokBarang servicesBarang) throws RemoteException{
        TableModel model = tblBarang1.getModel();
        int index = tblBarang1.getSelectedRow();
        int jumlah = Integer.valueOf(tJumlah1.getText());
        int jumlah1 = Integer.valueOf(model.getValueAt(index, 2).toString());
        jumlah = jumlah1 - jumlah;
        StokBarang b=new StokBarang();
        b.setKode(Integer.valueOf(tKode1.getText()));
        b.setNama(tNama1.getText());
        b.setJumlah(jumlah);
        servicesBarang.update(b);

    }
    
