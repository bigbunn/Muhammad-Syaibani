    private void bInput1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInput1ActionPerformed
        // TODO add your handling code here:
        if (tKode1.getText().equals("") || tNama1.getText().equals("")
        || tJumlah1.getText().equals(""))   {
            JOptionPane.showMessageDialog(null, "Silahkan Input Data Dulu");
        }
        else{
            try {
                update(servicesBarang);
                select(tblBarang1);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            } 
        }
        clear();
    }//GEN-LAST:event_bInput1ActionPerformed

