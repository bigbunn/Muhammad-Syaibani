    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nilai1 = txtNilai1.getText();
        String nilai2 = txtNilai2.getText();
        String nilai3 = txtNilai3.getText();
        
        try {
            Connection c = Koneksi.getKoneksi(); 
            String sql = "INSERT INTO DIAGRAMPIE (nilai1,nilai2,nilai3) VALUES (?,?,?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1,nilai1);
            p.setString(2,nilai2);
            p.setString(3,nilai3);
            p.executeUpdate();
            p.close();
        }   catch (SQLException e)  {
            System.out.println("Terjadi Kesalahan"); 
        }
        refreshTabel();
        loadData();
    }//GEN-LAST:event_jButton2ActionPerformed

