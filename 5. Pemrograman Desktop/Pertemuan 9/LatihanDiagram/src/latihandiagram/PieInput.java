    private void loadData(){
        try{ 
            Connection c = Koneksi.getKoneksi(); 
            Statement s = c.createStatement(); 
        
            String sql = "SELECT * FROM DIAGRAMBAR"; 
            ResultSet r = s.executeQuery(sql);
        while (r.next()) { 
            // lakukan penelusuran baris 
            String nilai1 = r.getString("nilai1");
            String nilai2 = r.getString("nilai2");
            String nilai3 = r.getString("nilai3");
            
            String tbData[] = {nilai1, nilai2, nilai3};
            
            DefaultTableModel tblModel = (DefaultTableModel)tabelNilai.getModel();
            tblModel.addRow(tbData);
            } 
        r.close(); 
        s.close(); 
        } catch(SQLException e) { 
            System.out.println("Terjadi Kesalahan"); 
        } 
    }

    private void refreshTabel(){
        DefaultTableModel model =(DefaultTableModel) 
        tabelNilai.getModel();
        while(model.getRowCount()>0){
            model.setRowCount(0);
        } 
    }
    
    private void refreshText(){
        txtNilai1.setText("");
        txtNilai2.setText("");
        txtNilai3.setText("");
    }
    
