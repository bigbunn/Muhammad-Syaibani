    private void jSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSimpanActionPerformed
        // TODO add your handling code here:
        String nip = textNip.getText();
        String nama = textNama.getText(); 
        String jabatan = (String)cJabatan.getSelectedItem();
        String alamat = textAlamat.getText(); 
        String email = textEmail.getText();
        String notelp = textNotelp.getText();
        
        try{ 
            Connection c = Config.getKoneksi(); 
            String sql = "INSERT INTO KARYAWAN(nip,nama,jabatan,alamat,email,no_telp) VALUES (?, ?, ?, ?, ?,?)"; 
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nip); 
            p.setString(2, nama);
            p.setString(3, jabatan);
            p.setString(4, alamat); 
            p.setString(5, email); 
            p.setString(6, notelp);
            p.executeUpdate();
            p.close(); 
        } catch(SQLException e) { 
            System.out.println("Terjadi Kesalahan"); 
        }
        refreshTabel();
        loadData();
    }//GEN-LAST:event_jSimpanActionPerformed

