    private void loadData(){
        try{ 
            Connection c = Config.getKoneksi(); 
            Statement s = c.createStatement(); 
        
            String sql = "SELECT * FROM KARYAWAN"; 
            ResultSet r = s.executeQuery(sql); 
        while (r.next()) { 
            // lakukan penelusuran baris 
            String no = r.getString("no");
            String nip = r.getString("nip");
            String nama = r.getString("nama");
            String jabatan = r.getString("jabatan");
            String alamat = r.getString("alamat");
            String email = r.getString("email");
            String notelp = r.getString("no_telp");
            
            String tbData[] = {no,nip,nama,jabatan,alamat,email,notelp};
            
            DefaultTableModel tblModel = (DefaultTableModel)tabelKaryawan.getModel();
            tblModel.addRow(tbData);
            } 
        r.close(); 
        s.close(); 
        } catch(SQLException e) { 
            System.out.println("Terjadi Kesalahan"); 
        } 
    }

    private void refreshTabel(){
        DefaultTableModel model =(DefaultTableModel) 
        tabelKaryawan.getModel();
        while(model.getRowCount()>0){
            model.setRowCount(0);
        } 
    }
    
