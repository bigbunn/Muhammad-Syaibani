    private void menuMakanan(){
        
        if (cNasiGoreng.isSelected()){
            hargaMenu = 15000;
            jumlah = Integer.parseInt(jumlahNasi.getText().trim());
            subTotal1 = hargaMenu * jumlah;
            
        } 
        if (cSateAyam.isSelected()){
            hargaMenu = 17000;
            jumlah = Integer.parseInt(jumlahSate.getText().trim());
            subTotal2 = hargaMenu * jumlah;
        } 
        if (cSotoAyam.isSelected()){
            hargaMenu = 16000;
            jumlah = Integer.parseInt(jumlahSoto.getText().trim());
            subTotal3 = hargaMenu * jumlah;
        }
        
        total = subTotal1 + subTotal2 + subTotal3;
        labelTotal.setText(strTotal + total);
    }
    
    private void cekMakanan(){
        if (cNasiGoreng.isSelected()){
            jumlahNasi.setEnabled(true);
        } else {
            subTotal1=0;
            jumlahNasi.setEnabled(false);
        }
        
        if (cSateAyam.isSelected()){
            jumlahSate.setEnabled(true);
        } else {
            subTotal2=0;
            jumlahSate.setEnabled(false);
        }
        
        if (cSotoAyam.isSelected()){
            jumlahSoto.setEnabled(true);
        } else {
            subTotal3=0;
            jumlahSoto.setEnabled(false);
        }
    }
