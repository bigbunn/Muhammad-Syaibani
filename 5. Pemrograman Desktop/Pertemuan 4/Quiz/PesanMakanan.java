    private void tombolBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolBayarActionPerformed
        // TODO add your handling code here:
        bayar = Integer.parseInt(jumlahBayar.getText().trim());
        kembalian = bayar - total;
        if (kembalian > 0){
            labelKembalian.setText(strKembalian + kembalian);
        } else if (kembalian < 0){
            labelKembalian.setText("Pembayaran kurang");
        } else if (kembalian == 0){
            labelKembalian.setText("Tidak ada kembalian");
        } else{
            labelKembalian.setText("Masukkan jumlah kembalian");
        }
    }//GEN-LAST:event_tombolBayarActionPerformed

