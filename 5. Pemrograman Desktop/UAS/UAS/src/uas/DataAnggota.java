    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        if (tNpm.getText().equals("") || tNama.getText().equals("") 
                || tTempatLahir.getText().equals("") || tTanggalLahir.getText().equals("")
                || tProdi.getText().equals(""))  {
            JOptionPane.showMessageDialog(null, "Silahkan Input Data Dulu");
        }
        else{
            try {       
                String npm = tNpm.getText();
                String nama = tNama.getText();
                String tempatLahir = tTempatLahir.getText();
                String tanggalLahir = tTanggalLahir.getText();
                String prodi = tProdi.getText();
                
                Connection c = Koneksi.getConnection();
                String sql = "INSERT INTO dataanggota (NPM,Nama,TempatLahir,TanggalLahir"
                        + ",prodi) VALUES (?,?,?,?,?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, npm);
                p.setString(2, nama);
                p.setString(3, tempatLahir);
                p.setString(4, tanggalLahir);
                p.setString(5, prodi);
                p.executeUpdate();
                p.close();
                refreshTabel();
                loadAnggota();
                clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            } 
        }
    }//GEN-LAST:event_bSimpanActionPerformed

