    private void loadAnggota() {
        try {
            Connection c = Koneksi.getConnection();
            Statement s = c.createStatement();
            String sql = "SELECT * from dataanggota";
            ResultSet r = s.executeQuery(sql);
            while (r.next())    {
                String npm = r.getString("NPM");
                String nama = r.getString("Nama");
                String tempatLahir = r.getString("TempatLahir");
                String tanggalLahir = r.getString("TanggalLahir");
                String prodi = r.getString("Prodi");
                String tbData[] = {npm,nama,tempatLahir,tanggalLahir,prodi};
                DefaultTableModel tblModel = (DefaultTableModel)tblBarang.getModel();
                tblModel.addRow(tbData);
            }
            r.close(); 
            s.close(); 
        }   catch(SQLException e) { 
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan!");
        }
    }
    
    private void refreshTabel() {
        DefaultTableModel model = (DefaultTableModel)
        tblBarang.getModel();
        while(model.getRowCount()>0){
            model.setRowCount(0);
        }
    }
    
