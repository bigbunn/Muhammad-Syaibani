    private void loadBuku() {
        try {
            Connection c = Koneksi.getConnection();
            Statement s = c.createStatement();
            String sql = "SELECT * from databuku";
            ResultSet r = s.executeQuery(sql);
            while (r.next())    {
                String kode = r.getString("KodeBuku");
                String judul = r.getString("JudulBuku");
                String jenis = r.getString("Jenis");
                String tglTerbit = r.getString("TglTerbit");
                String pengarang = r.getString("Pengarang");
                String stok = r.getString("StokBuku");
                String tbData[] = {kode,judul,jenis,tglTerbit,pengarang,stok};
                DefaultTableModel tblModel = (DefaultTableModel)tblBarang.getModel();
                tblModel.addRow(tbData);
            }
            r.close(); 
            s.close(); 
        }   catch(SQLException e) { 
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan!");
        }
    }
    
    private void refreshTabel() {
        DefaultTableModel model = (DefaultTableModel)
        tblBarang.getModel();
        while(model.getRowCount()>0){
            model.setRowCount(0);
        }
    }
    
