                String stok = tStok.getText();
                
                Connection c = Koneksi.getConnection();
                String sql = "update databuku set JudulBuku = ?, Jenis = ?, TglTerbit = ?"
                        + ", Pengarang = ?, StokBuku = ? where KodeBuku = ?";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, judul);
                p.setString(2, jenis);
                p.setString(3, tglTerbit);
                p.setString(4, pengarang);
