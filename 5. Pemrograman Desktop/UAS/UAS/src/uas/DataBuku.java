    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        if (tKode.getText().equals("") || tJudul.getText().equals("") 
                || tJenis.getText().equals("") || tTanggalTerbit.getText().equals("")
                || tPengarang.getText().equals("") || tStok.getText().equals(""))  {
            JOptionPane.showMessageDialog(null, "Silahkan Input Data Dulu");
        }
        else{
            try {       
                String kode = tKode.getText();
                String judul = tJudul.getText();
                String jenis = tJenis.getText();
                String tglTerbit = tTanggalTerbit.getText();
                String pengarang = tPengarang.getText();
                String stok = tStok.getText();
                
                Connection c = Koneksi.getConnection();
                String sql = "INSERT INTO databuku (KodeBuku,JudulBuku,Jenis,TglTerbit,Pengarang"
                        + ",StokBuku) VALUES (?,?,?,?,?,?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setInt(1, Integer.parseInt(kode));
                p.setString(2, judul);
                p.setString(3, jenis);
                p.setString(4, tglTerbit);
                p.setString(5, pengarang);
                p.setString(6, stok);
                p.executeUpdate();
                p.close();
                refreshTabel();
                loadBuku();
                clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            } 
        }
    }//GEN-LAST:event_bSimpanActionPerformed

