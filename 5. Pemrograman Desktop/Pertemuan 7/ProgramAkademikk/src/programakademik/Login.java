    private String enkripsi(String text){
        String key = tKey.getText();
        String isiText = (key.length()) + key + text;
        String out = "";
        for (int i =0;i<isiText.length();i++){
        int index = isiText.charAt(i);
        char s = (char) (index + 1);
        out = out + String.valueOf(s);
        }
        return out;
    }
    
