    private void loadBarang() {
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * from stokbarang";
            ResultSet r = s.executeQuery(sql);
            while (r.next())    {
                String kode = r.getString("kode");
                String nama = r.getString("nama");
                String jumlah = r.getString("jumlah");
                String tbData[] = {kode,nama,jumlah};
                DefaultTableModel tblModel = (DefaultTableModel)tblBarang1.getModel();
                tblModel.addRow(tbData);
            }
            r.close(); 
            s.close(); 
        }   catch(SQLException e) { 
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan!");
        }
    }
    
    private void refreshTabel() {
        DefaultTableModel model = (DefaultTableModel)
        tblBarang1.getModel();
        while(model.getRowCount()>0){
            model.setRowCount(0);
        }
    }
    
