    private void bInput1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInput1ActionPerformed
        // TODO add your handling code here:
        int index = tblBarang1.getSelectedRow();
        TableModel model = tblBarang1.getModel();
        int jumlah1 = Integer.parseInt(model.getValueAt(index, 2).toString());
        
        String kode = tKode1.getText();
        int jumlah = Integer.parseInt(tJumlah1.getText());
        jumlah = jumlah1 - jumlah;
        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "UPDATE stokbarang SET jumlah = ? where kode = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setInt(1, jumlah);
            p.setString(2, kode);                       
            p.executeUpdate();
            p.close();
        }   catch (SQLException e) {
            System.out.println("Terjadi Kesalahan");
        }
        refreshTabel();
        loadBarang();
        refreshText();
    }//GEN-LAST:event_bInput1ActionPerformed

